
import java.util.*;
public class Type_casting{
    public static void main(String args[]){
       // Scanner sc=new Scanner(System.in);
        float a=25.66f;
        int b=(int)a;           // TO make Float to int 
        System.out.println(b);

        char c='a'; 
        int num=c;              //takes the ASCII value
        System.out.println(num);
    }
}