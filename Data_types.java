public class Data_types{
    public static void main(String args[]){
        byte b=10;
        System.out.println(b);
        char ch='m';            //only single character
        System.out.println(ch);
        boolean var=true;
        System.out.println(var);
        int a=10;
        System.out.println(a);
        Float f=3.14f;          //For floating we take f otherwise it takes as double
        System.out.println(f);
        //long-  for large integer value
        //double-  for large floating value
        
    }
}